dim=Length[matMR];
colset=Range[dim];
rowset=colset;
$RecursionLimit=\[Infinity];
rcoverc=rowset;
ccoverc=colset;
zstarset={};
zpset={};
{zpi,zpj}={0,0};
matblock={};
newmin={};
newzeropos={};
OptimalBipartiteGraphMatching["step1"]:=(
minR=Min[#]&/@matMR;
matMR=Chop[matMR-minR];
OptimalBipartiteGraphMatching["step2"];
);
OptimalBipartiteGraphMatching["step2"]:=
(
zmat=matMR/.{0-> 1,x_/;x!=0-> 0};
zeroset=Position[zmat,1];
While[rowset!={},
rpos=First[rowset];
zpos=First@FirstPosition[zmat[[rpos,colset]],1];
If[IntegerQ[zpos],
cpos=colset[[zpos]];
colset=DeleteCases[colset,cpos];
rowset=Complement[rowset,Flatten[Position[zmat[[All,cpos]],1]]];
AppendTo[zstarset,Join[{rpos},{cpos}]],
rowset=DeleteCases[rowset,rpos];
];
];
{zstari,zstarj}=First[zstarset];
OptimalBipartiteGraphMatching["step3"];
);
OptimalBipartiteGraphMatching["step3"]:=(
ccoverc=Complement[ccoverc,zstarset[[All,2]]];
If[MatchQ[ccoverc,{}],
done=True;
zstarset,
OptimalBipartiteGraphMatching["step4"];
]
);
OptimalBipartiteGraphMatching["step4"]:=(
done=False;
While[!MatchQ[done, True],
If[MatchQ[#,{_,_}],
{zpi,zpj}=({rcoverc[[#1]],ccoverc[[#2]]}&@@#);
AppendTo[zpset,{zpi,zpj}];,
done=True;
OptimalBipartiteGraphMatching["step6"];
]&@finduncoveredzero[];
If[MatchQ[#,{0,0}],
done=True;
OptimalBipartiteGraphMatching["step5"];,
{zstari,zstarj}=#;
rcoverc=DeleteCases[rcoverc,zstari];
ccoverc=Union[ccoverc,{zstarj}];
]&@(If[MatchQ[#,{_,_}],#,{0,0}]&@findstarredzeroinrow[]);
]
);
OptimalBipartiteGraphMatching["step5"]:=(
zstartrack={};
zptrack={};
AppendTo[zptrack,Last[zpset]];
AppendTo[zstartrack,FirstCase[zstarset,{_,Last[zptrack][[2]]}]];
While[MatchQ[Last[zstartrack],{_,_}],
AppendTo[zptrack,FirstCase[zpset,{Last[zstartrack][[1]],_}]];
AppendTo[zstartrack,FirstCase[zstarset,{_,Last[zptrack][[2]]}]];
];
zstarset=zstarset/.Thread[zstartrack-> Nothing];
zstarset=Join[zstarset,zptrack];
zpset={};
zptrack={};
zstartrack={};
rcoverc=Range[dim];
ccoverc=Range[dim];
OptimalBipartiteGraphMatching["step3"];
);
OptimalBipartiteGraphMatching["step6"]:=(
rcover=Complement[Range[dim],rcoverc];
matblock=matMR[[rcoverc,ccoverc]];
