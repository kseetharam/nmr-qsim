	Flatten[#,1]& @
		Map[
			Function[arow,
				Map[
					Function[brow,
						Flatten[#,2]&@{ 
							Map[
								Function[acol,		
									acol*brow
								],
								arow
							] 
						}
					],
					matB
				]
			],
			matA
		]


