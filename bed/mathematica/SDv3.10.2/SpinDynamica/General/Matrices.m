NDiagonalize[mat_?NumericMatrixQ,opts___Rule]:=Module[{evals,evecs,X,Xinv,ass,orthogevecs,degevals,degevalpositions},(
ass=Assumptions/.{opts}/.Options[NDiagonalize];
orthogevecs=
Switch[ass,
None|False|Automatic,False,
_Rule|{_Rule...},
	OrthogonalEigenvectors/.Flatten[{ass,OrthogonalEigenvectors->False}],
_,False
];
{evals,evecs}=Eigensystem[If[Length[mat]>=256,N@Normal@mat,N@mat]];
If[orthogevecs,
(degevals=First/@Select[Tally[evals,(Chop[#1-#2]==0)&],(Last[#]>1)&];
degevalpositions=(Flatten@Position[Chop[evals-#],0])&/@degevals;
evecs=ReplacePart[evecs,Flatten[MapThread[Rule,{#,Orthogonalize[evecs[[#]]]}]&/@degevalpositions]]
)
];
X=SparseArray@Chop@Transpose[Normalize/@evecs];
Xinv=If[orthogevecs,
ConjugateTranspose[X],
(If[Chop@Det[X]==0,
(Message[NDiagonalize::sing];$Failed),
SparseArray@Inverse[X]]
)];
Switch[Xinv,
_Inverse|_PseudoInverse,(Message[NDiagonalize::fail];$Failed),
_,{X,evals,Xinv}
]
)];


