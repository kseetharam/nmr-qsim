(* ::Input::Initialization:: *)
DefineBasis[basis_,kets_List,opts___Rule]:=
Module[{check,basislabels,unasssymbs},(
check=CheckBasis/.{opts}/.Options[DefineBasis];
basislabels=BasisLabels/.{opts}/.Options[DefineBasis];
unasssymbs=UnassignedSymbols[kets];
If[Length[unasssymbs]!=0,
(Message[DefineBasis::unasssymb,unasssymbs];
check=False)];
BasisKets[basis]=kets;
BasisBras[basis]=Adjoint/@kets;
BasisDimension[basis]=Length[kets];
BasisLabels[basis]=basislabels;
Which[
check&&(CheckedBasisQ[basis]||CheckBasis[basis]),
	(
	DefinedBasisQ[basis]=True;
	CheckedBasisQ[basis]=True;
	SetOptions[UnassignedSymbols,
	PermittedSymbols->Append[PermittedSymbols/.Options[UnassignedSymbols],basis]];
	SpinSystem[basis]^=SpinSystem[];
	Message[DefineBasis::defined,basis];
	),
check&&!CheckedBasisQ[basis]&&!CheckBasis[basis],
	(Message[DefineBasis::notdefined,basis];
	UndefineBasis[basis];
	DefinedBasisQ[basis]=False;
	$Failed
	),
True,
	(Message[DefineBasis::nocheck,basis];
	Message[DefineBasis::defined,basis];
	DefinedBasisQ[basis]=True;
	CheckedBasisQ[basis]=False;
	SpinSystem[basis]^=SpinSystem[];
	)
]
)];


