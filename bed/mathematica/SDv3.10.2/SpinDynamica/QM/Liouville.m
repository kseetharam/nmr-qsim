DefineOperatorBasis[newopbasis_,opbasis_?DefinedOperatorBasisQ,sop_?SuperoperatorQ,ordering:{Except[_?OperatorQ]..},opts___Rule]:=
Module[{spinsys,basis,permmat,NL},(
spinsys=SpinSystem[opbasis];
basis=ZeemanBasis[spinsys];
NL=OperatorBasisDimension[opbasis];
BasisOperators[newopbasis]=ExpressOperator[sop[#],opbasis]&/@BasisOperators[opbasis][[ordering]];
permmat=SparseArray@ReplacePart[
DiagonalMatrix[Table[0,{NL}]],
Inner[({#1,#2}->1)&,ordering,Range[1,NL],List]
];
OperatorBasisTransformationMatrix[newopbasis,opbasis,basis]=
permmat . Adjoint@SuperoperatorMatrixRepresentation[sop,opbasis,basis];
IntermediateOperatorBasis[newopbasis]=opbasis;
CheckOperatorBasis[newopbasis]=CheckOperatorBasis[opbasis]&&UnitaryMatrixQ@SuperoperatorMatrixRepresentation[sop,opbasis];
SetOptions[
UnassignedSymbols,
PermittedSymbols->AppendTo[PermittedSymbols/.Options[UnassignedSymbols],newopbasis]];
DefinedOperatorBasisQ[newopbasis]=True;
SpinSystem[newopbasis]^=SpinSystem[opbasis];
Message[DefineOperatorBasis::defined,newopbasis];
newopbasis
)];


