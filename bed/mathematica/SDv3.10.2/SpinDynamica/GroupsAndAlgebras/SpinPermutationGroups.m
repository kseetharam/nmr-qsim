dim\[CapitalGamma]s=Length@\[CapitalGamma]\[Gamma]subspaces[[First@First@Position[\[CapitalGamma]\[Gamma]subspaces,First[#]]]]&/@\[CapitalGamma]\[Gamma]spaces;
IsotopicSpaces=MapThread[Partition[#1,#2]&,{\[CapitalGamma]\[Gamma]spaces,dim\[CapitalGamma]s}];

phasetransformations=ConsistentIsotopicPhase[G\[CapitalGamma]\[Gamma]mats,IsotopicSpaces[[#]],dim\[CapitalGamma]s[[#]],dimG]&/@Range[Length[dim\[CapitalGamma]s]];
phasetransformation=SparseArray@Simplify@Normal@ArrayFlatten[ReleaseHold@DiagonalMatrix[Hold/@phasetransformations]];
V=SparseArray[ConjugateTranspose[(FullSimplify[Abs[#]]Exp[I FullSimplify[Arg[#]]]&/@(X\[CapitalGamma]\[Gamma]sorted . ConjugateTranspose[phasetransformation]))]];

dimvec=Flatten[ConstantArray[dim\[CapitalGamma]s[[#]],Length[Flatten[IsotopicSpaces[[#]]]]]&/@Range[Length[dim\[CapitalGamma]s]]];
{K3\[CapitalGamma],K4\[CapitalGamma]}=Normal@Chop@N@{V . MatrixRepresentation[K3,basis1] . ConjugateTranspose[V],V . MatrixRepresentation[K4,basis1] . ConjugateTranspose[V]};
V[[Ordering[Chop@N@Transpose[{dimvec,Diagonal[K3\[CapitalGamma]],-Diagonal[K4\[CapitalGamma]]}]]]]
]


