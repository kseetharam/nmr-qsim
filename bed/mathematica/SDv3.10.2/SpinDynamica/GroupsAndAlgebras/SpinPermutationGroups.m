\[CapitalGamma]\[Gamma]cuts=Table[{1+#[[i]],#[[i+1]]},{i,1,Length[#]-1}]&@Join[{0},Accumulate[Length/@GatherBy[\[Xi]vec\[CapitalGamma]\[Gamma]sorted,_?(Chop[#1-#2]==0&)]]];
