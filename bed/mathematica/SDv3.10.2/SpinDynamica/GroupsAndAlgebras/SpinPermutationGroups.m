\[Xi]vec=Normal[Xinv . (MatrixRepresentation[CSCO[[1]],basis1]) . X//N//Chop//Diagonal];

G\[CapitalGamma]\[Gamma]mats=Simplify[Chop[Xinv . # . X]]&/@Gmats;
\[CapitalGamma]\[Gamma]subspaces=SortBy[ConnectedComponents[Rule@@@Chop[Total[RandomReal[{-100,100},dimG]*G\[CapitalGamma]\[Gamma]mats]]["NonzeroPositions"]],Length];
