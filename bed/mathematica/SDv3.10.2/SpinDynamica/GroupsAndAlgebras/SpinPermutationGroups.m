spins,
K1,K2,K3,K4,CSCO,G,dimG,
ev,vecs,Gmats,X,Xinv,\[Xi]vec,
G\[CapitalGamma]\[Gamma]mats,\[CapitalGamma]\[Gamma]subspaces,\[CapitalGamma]\[Gamma]order,
vecs\[CapitalGamma]\[Gamma]sorted,X\[CapitalGamma]\[Gamma]sorted,X\[CapitalGamma]\[Gamma]invsorted,\[Xi]vec\[CapitalGamma]\[Gamma]sorted,
\[CapitalGamma]\[Gamma]cuts,\[CapitalGamma]\[Gamma]bounds,\[CapitalGamma]\[Gamma]spaces,
