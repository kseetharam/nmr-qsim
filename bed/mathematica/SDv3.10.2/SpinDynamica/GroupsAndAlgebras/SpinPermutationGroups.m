SymmetryAdaptedBasisTFM[SymmetryAdaptedBasis[spinsys_,SpinPermutationGroupOperators[{{grname_,n_},gops_},_],opts___Rule],basis1_]:=Module[{
spins,nspins,dimG,
PSGZKB,SG,SX,
K1,K2,KC1,KC2,
nspace1,nspace2,nspace,
nops,nopsZKB,nopssym,
ev,vecs,X,
Gmats,Gmats\[Xi],subspaces,
\[Xi]vecs,\[Xi]groupings,\[Xi]remaining,\[Xi]group,\[Xi]perm,\[Xi]dim,
Gmats\[Xi]perm,phaseU,normphaseU
},
spins=First[Transpose[spinsys]];
nspins=Length[spins];
dimG=Length[gops];
Gmats=MatrixRepresentation[#,basis1]&/@gops;

SG=(LeftMultiplicationSuperoperator[#]) . (RightMultiplicationSuperoperator[Adjoint[#]])&/@gops;
PSGZKB=SuperoperatorMatrixRepresentation[Total[SG],ZeemanKetBraOperatorBasis[spinsys]];
SX=OperatorBasisTransformationMatrix[OperatorBasis[],ZeemanKetBraOperatorBasis[spinsys]];

K1=(BasisOperators[OperatorBasis[]]) . (SX . PSGZKB . OperatorVectorRepresentation[opI[spins,"z"],ZeemanKetBraOperatorBasis[spinsys]]);
K2=(BasisOperators[OperatorBasis[]]) . (SX . PSGZKB . OperatorVectorRepresentation[Sum[(opI[i]) . (opI[j]),{i,1,nspins},{j,1,nspins}],ZeemanKetBraOperatorBasis[spinsys]]);

KC1=SX . SuperoperatorMatrixRepresentation[CommutationSuperoperator[K1],ZeemanKetBraOperatorBasis[spinsys]] . ConjugateTranspose[SX]//Simplify;
KC2=SX . SuperoperatorMatrixRepresentation[CommutationSuperoperator[K2],ZeemanKetBraOperatorBasis[spinsys]] . ConjugateTranspose[SX]//Simplify;

nspace1=SparseArray/@NullSpace[KC1];
nspace2=SparseArray/@NullSpace[KC2];
nspace=VectorSpaceIntersection[nspace1,nspace2];

nops=(BasisOperators[OperatorBasis[]]) . #&/@nspace;
