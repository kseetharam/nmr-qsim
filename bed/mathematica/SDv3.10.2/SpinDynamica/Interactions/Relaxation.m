PhenomenologicalRelaxationSuperoperator::usage=
"PhenomenologicalRelaxationSuperoperator generates a relaxation superoperator according to a simple phenomenological relaxation model in which the uncorrelated relaxations of individual spins are characterised by relaxation time constants T1 and T2. PhenomenologicalRelaxationSuperoperator[T] uses the same relaxation time constant T for all spins, with T1=T2=T. PhenomenologicalRelaxationSuperoperator[T1,T2] uses the same relaxation time constants T1 and T2 for all spins. The syntax PhenomenologicalRelaxationSuperoperator[{{j,Tj},{k,Tk}..}] specifies an individual relaxation time constant T1=T2=Tj for spin j, and similar for spin k, etc. PhenomenologicalRelaxationSuperoperator[{{j,T1j,T2j},{k,T1k,T2k}..}] specifies individual relaxation time constants T1j and T2j for spin j, and similar for spin k, etc.";


