(* ::Input::Initialization:: *)
PhenomenologicalRelaxationSuperoperator[jT1T2list:{{_,_,_}..},opts___Rule]:=
Module[{bops,counts,spins,spinsmissing,jT1T2list$,R1,R2,diagels},(
bops=BasisOperators[ShiftAndZOperatorBasis[]];
spins=First/@SpinSystem[];
spinsmissing=Complement[spins,jT1T2list[[All,1]]];
jT1T2list$=Join[jT1T2list,{#,\[Infinity],\[Infinity]}&/@spinsmissing];
counts=Function[op,
{#,{"z",Count[{op},HoldPattern@opI[#,"z",___],Infinity]},{"+",Count[{op},HoldPattern@opI[#,"+",___],Infinity]},
{"-",Count[{op},HoldPattern@opI[#,"-",___],Infinity]}}&/@
spins
]/@bops;
R1[j_]:=1/Cases[jT1T2list$,{j,_,_}][[1,2]];
R2[j_]:=1/Cases[jT1T2list$,{j,_,_}][[1,3]];
diagels=-Apply[
Plus,
counts/.{j_,{"z",nz_},{"+",np_},{"-",nm_}}:>nz*R1[j]+np*R2[j]+nm*R2[j],
{1}
];
Superoperator[SparseArray[DiagonalMatrix[diagels]],ShiftAndZOperatorBasis[]]
)];


