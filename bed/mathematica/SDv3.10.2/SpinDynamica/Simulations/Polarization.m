PolarizationLevelOperator::usage=
"PolarizationLevelOperator is an operator for the polarization level of the spin system. For example OperatorAmplitude[\[Rho]\[Rule]PolarizationLevelOperator[{1,2},\"z\"]] extracts the mean polarization level of spins 1 and 2 along the z-axis, for the density operator \[Rho].
PolarizationLevelOperator[\"z\"] is the operator for the mean level of polarization along the z-axis. 
PolarizationLevelOperator[{1,2},\"z\"] is the operator for the the mean level of polarization of spins 1 and 2 along the z-axis. 
The general syntax is PolarizationLevelOperator[{j,k...},axis] where {j,k..} is the set of spins to be analyzed and axis defines the direction of the polarization. 
PolarizationLevelOperator may be used as an observable in simulation routines (Trajectory, TransformationAmplitude, etc.) in order to simulate the polarization level of the spin system. 
The operator PolarizationLevelOperator is distinct from the \"polarization operator\" used by Ernst and co-workers, which refers to the projection operator onto the Zeeman eigenstates of a spin-1/2, and whose functionality is covered in SpinDynamica by the syntax opI[j,\"\[Alpha]\"] and opI[j,\"\[Beta]\"].
";


