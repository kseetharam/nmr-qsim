PolarizationLevelOperator[j:Except[_List],axis_?ValidAxisSpecificationQ,spinsys_]:=
(PolarizationLevelOperator[axis,j,spinsys]=
Module[{Ij,dim},(
Ij=SpinQuantumNumber[j,spinsys];
dim=BasisDimension[ZeemanBasis[spinsys]];
Simplify[3/((Ij+1)*dim)*opI[j,axis]]
)]
);


