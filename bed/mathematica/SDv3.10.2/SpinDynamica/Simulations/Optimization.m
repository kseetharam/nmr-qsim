MaximizeTransformationAmplitude[
\[Rho]ini_->obs_,
events_,
varspecs:{{_,_}..},
opts:(_Rule|_RuleDelayed)...
]:=
Module[{negta,minval,varrules,vars,varinivals,nvar,method,constrpos},(
{vars,varinivals}=Transpose[varspecs];
nvar=Length[vars];
negta[Repeated[_?NumericQ,{nvar}]]:=
-TransformationAmplitude[\[Rho]ini->obs,
events,
Evaluate[Sequence@@FilterRules[{opts},Options[TransformationAmplitude]]]
];
Message[MaximizeTransformationAmplitude::unconstr];
method=
Method/.{opts}/.Options[MaximizeTransformationAmplitude]/.{Automatic->"InteriorPoint"};
If[!MatchQ[method,Automatic],Message[MaximizeTransformationAmplitude::meth,method]];
Check[
({minval,varrules}=
FindMinimum[
negta[Sequence@@vars],
varspecs,
Evaluate[Sequence@@FilterRules [
Join[
{Method->method,opts},
Options[MaximizeTransformationAmplitude]
],
Options[FindMinimum]
]
]
];
{-minval,varrules}),
(Message[MaximizeTransformationAmplitude::fail];
$Failed),
{Event::negdur,Delay::negdur,Pulse::inval\[Beta],Pulse::inval\[Phi],Pulse::neg\[Tau],ShapedPulse::invaldur,FindMinimum::nrnum}
]
)/;(
constrpos=ConstrainPositive/.{opts}/.Options[MaximizeTransformationAmplitude];
MatchQ[constrpos,False]
)];


