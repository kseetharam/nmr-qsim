MaximizeTransformationAmplitude[
\[Rho]ini_->obs_,
events_,
varspecs:{{_,_,{_,_}}..},
opts:(_Rule|_RuleDelayed)...
]:=
Module[{negta,minval,varrules,vars,varinivals,nvar,method,varbounds,varminvals,varmaxvals,constraints},(
{vars,varinivals,varbounds}=Transpose[varspecs];
{varminvals,varmaxvals}=Transpose[varbounds];
constraints=And@@MapThread[(#1<=#2<=#3)&,{varminvals,vars,varmaxvals}];
Message[MaximizeTransformationAmplitude::constr,constraints];nvar=Length[vars];
negta[vars:Repeated[_?NumericQ,{nvar}]]:=
Module[{varvals,reflvarvals},(
varvals={vars};
reflvarvals=MapThread[
Which[
#1>#2,#1+Abs[#2-#1],
#2>#3,#3-Abs[#2-#3],
True,#2]&,
{varminvals,varvals,varmaxvals}
];
-TransformationAmplitude[\[Rho]ini->obs,
events,
Evaluate[Sequence@@FilterRules[{opts},Options[TransformationAmplitude]]]
]/.MapThread[(#1->#2)&,{{vars},reflvarvals}]
)];
method=
Method/.{opts}/.Options[MaximizeTransformationAmplitude];
If[!MatchQ[method,Automatic],Message[MaximizeTransformationAmplitude::meth,method]];
Check[
({minval,varrules}=
FindMinimum[
{negta[Sequence@@vars],constraints},
Transpose[{vars,varinivals}],
Evaluate[Sequence@@FilterRules[
Join[
{Method->method,opts},
Options[MaximizeTransformationAmplitude]
],
Options[FindMinimum]
]
]
];
{-minval,varrules}),
(Message[MaximizeTransformationAmplitude::fail];
$Failed),
{Event::negdur,Delay::negdur,Pulse::inval\[Beta],Pulse::inval\[Phi],Pulse::neg\[Tau],ShapedPulse::invaldur,FindMinimum::nrnum}
]
