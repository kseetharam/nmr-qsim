AxesToEuler::usage = 
	"AxesToEuler[system2,system1,opts] gives the Euler angles \[CapitalOmega]21={\[Alpha]21,\[Beta]21,\[Gamma]21} by which each axis of system2 may be rotated around the system1 axes in order to be brought into coincide with the corresponding axis of system1:
		e\[Micro]1=Rz1(\[Alpha]21)Ry1(\[Beta]21)Rz1(\[Gamma]21).e\[Micro]2, where \[Mu]\[Element]{x,y,z}.
The same result is generated by rotating around the system2 axes:
		e\[Micro]1=Rz2(\[Alpha]21)Ry2(\[Beta]21)Rz2(\[Gamma]21).e\[Micro]2, where \[Mu]\[Element]{x,y,z}.
The same result is also generated by using \"moving axes\", where the axes follow the Euler rotations, but in this case the angles are applied in reverse order:
		e\[Micro]1=Rz2'(\[Gamma]21)Ry2'(\[Beta]21)Rz2(\[Alpha]21).e\[Micro]2, 
where the primed axes of system1 follow the rotation.  

In a typical application, system2 is the principal axis system P of an anisotropic interaction, and system1 is a reference axis system such as a molecular frame M, or the laboratory frame L. The routine AxesToEuler[P,M] then generates the Euler angles \[CapitalOmega]PM which would be used in spherical tensor realisations of the spin Hamiltonian. 

Each of system1 and system2 may have the form {Z} or {X,Z} or {X,Y,Z}. Here Z,X and Y are 3-vectors. If only Z is specified, the two remaining axes are constructed. If X and Z is specified, the z-axis of the constructed system is equal to Z and the x-axis is in the XZ plane. If all three axes are specified, they must be a right-handed orthogonal set.
If system1 is missing, the system {ex,ey,ez} is assumed.

AxesToEuler also accepts axis definitions containing symbolic angles; in this case the symbolic result assumes that all angles are in the first quadrant (i.e. greater than 0 and less than \[Pi]/2).
";


