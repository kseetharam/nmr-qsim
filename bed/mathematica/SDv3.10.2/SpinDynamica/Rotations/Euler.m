RotateEuler::usage =
	"RotateEuler[obj,{\[Alpha],\[Beta],\[Gamma]},axes] rotates the vector according to the Euler angles {\[Alpha],\[Beta],\[Gamma]} around the specified axes.

axes may have the form {z}, {x,z} or {x,y,z} (see routine AxisSystem).

If axes are missing, the system {ex,ey,ez} is assumed.
		
For example, RotateEuler[obj,{\[Alpha],\[Beta],\[Gamma]},{vx,vy,vz}] rotates obj by the angle \[Gamma] about the axis vz, followed by a rotation by the angle \[Beta] about vy, followed by a rotation through the angle \[Alpha] about vz.

The same rotations may be performed in the order \[Alpha]->\[Beta]->\[Gamma] about the axes vz->vy'->vz'' where the axes {vx,vy',vz''} are simultaneously rotated together with the object.

If the Euler angles relating two axis systems sys1 and sys2 are deduced using {\[Alpha]12,\[Beta]12,\[Gamma]12}=AxesToEuler[sys1,sys2], then the axis system sys1 may be superimposed on the axis system sys2 using the rotation RotateEuler[sys1,{\[Alpha]12,\[Beta]12,\[Gamma]12},sys1].

The object obj may be a 3-vector, an axis system, or contain lines, polygons, and text.
";


