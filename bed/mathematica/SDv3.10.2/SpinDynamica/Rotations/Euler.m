(* ::Input::Initialization:: *)
AxesToEuler[opts___Rule]:=AxesToEuler[{ex,ey,ez},opts];


(* ::Input::Initialization:: *)
AxesToEuler[system2:{{_,_,_}..},opts___Rule]:=
	AxesToEuler[system2,{ex,ey,ez},opts];


(* ::Input::Initialization:: *)
AxesToEuler[system2:{{_,_,_}..},system1:{{_,_,_}..},opts___Rule]/;And@@(NumericQ/@Flatten[{system1,system2}]):=
Module[{ex1,ey1,ez1,ex2,ey2,ez2,Rzz},(
{ex1,ey1,ez1}=AxisSystem@@system1;
{ex2,ey2,ez2}=AxisSystem@@system2;
Rzz=ez2.ez1;
RoundAngles/@
Which[
Chop@N@(Rzz-1)==0,
	{ArcTan[ex2.ex1,-ex2.ey1],0,0},
Chop@N@(Rzz+1)==0,
	{ArcTan[-ex2.ex1,-ex2.ey1],\[Pi],0},
True,
	{ArcTan[ex2.ez1,ey2.ez1],
	ArcCos[Rzz],
	ArcTan[-ez2.ex1,ez2.ey1]
	}
]
)];


(* ::Input::Initialization:: *)
AxesToEuler[system2:{{_,_,_}..},system1:{{_,_,_}..},opts___Rule]:=
Module[{ex1,ey1,ez1,ex2,ey2,ez2,Rzz,symbols,assumptions},(
{ex1,ey1,ez1}=AxisSystem@@system1;
{ex2,ey2,ez2}=AxisSystem@@system2;
symbols=Union@Cases[Flatten[{{ex1,ey1,ez1},{ex2,ey2,ez2}}],_Symbol,Infinity];
assumptions=And@@((0<#<\[Pi]/2)&/@symbols);
Rzz=ez2.ez1;
Refine@Simplify[
Which[
TrueQ[Chop@N@(Rzz-1)==0],
	{ArcTan[ex2.ex1,-ex2.ey1],0,0},
TrueQ[Chop@N@(Rzz+1)==0],
	{ArcTan[-ex2.ex1,-ex2.ey1],\[Pi],0},
True,
	{ArcTan[ex2.ez1,ey2.ez1],
	ArcCos[Rzz],
	ArcTan[-ez2.ex1,ez2.ey1]
	}
],
assumptions
]
)];


