(* ::Input::Initialization:: *)
RoundAngles[ang_?NumericQ]:= N[Mod[ang+Pi,2Pi]-Pi];
RoundAngles[ang_]:= ang;


(* ::Input::Initialization:: *)
EulerRotationMatrix[{a_,b_,g_}]:=
	{{Cos[a]Cos[b]Cos[g]-Sin[a]Sin[g],
			-Cos[a]Cos[b]Sin[g]-Sin[a]Cos[g],
			Cos[a]Sin[b]},
		{Sin[a]Cos[b]Cos[g]+Cos[a]Sin[g],
			-Sin[a]Cos[b]Sin[g]+Cos[a]Cos[g],
			Sin[a]Sin[b]},
		{-Sin[b]Cos[g], Sin[b]Sin[g], Cos[b]}};


RotateEuler[vec:{Except[_List],Except[_List],Except[_List]},
	{\[Alpha]_, \[Beta]_, \[Gamma]_},opts___Rule ]:=
	EulerRotationMatrix[{\[Alpha],\[Beta],\[Gamma]}].vec


RotateEuler[vec:{Except[_List],Except[_List],Except[_List]},\[CapitalOmega]:{\[Alpha]_,\[Beta]_,\[Gamma]_},axes_,opts___Rule ]:=
	Module[{\[CapitalOmega]FE},(
		\[CapitalOmega]FE = AxesToEuler[AxisSystem@@axes];
	Fold[RotateEuler[#1,#2]&,vec,{-Reverse[\[CapitalOmega]FE],\[CapitalOmega],\[CapitalOmega]FE}]
	)]


(* ::Input::Initialization:: *)
RotateEuler[obj_List,ang:{_, _, _},opts___]:=
		Map[ 
			RotateEuler[#,ang,opts]&, 
			obj 
		] 


RotateEuler[Graphics3D[obj___],\[CapitalOmega]:{\[Alpha]_,\[Beta]_,\[Gamma]_}]:=Graphics3D@GeometricTransformation[Normal@obj,{EulerRotationMatrix[\[CapitalOmega]],{0,0,0}}]


(* ::Input::Initialization:: *)
RotateEuler[obj_,ang:{_, _, _},opts___ ]:=
	obj/; !TrueQ[ Head[obj]==List ];


