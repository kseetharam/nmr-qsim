Module[{H\[Theta]mat,H\[Theta]evals,H\[Theta]evecs,expfacs,Z,htapprox,htapproxwth,numericH\[Theta]mat,X,Xinv},
{htapprox,htapproxwth}={HighTemperatureApproximation,HighTemperatureApproximationWarningThreshold}/.{opts}/.Options[ThermalEquilibriumDensityOperator];
H\[Theta]mat=MatrixRepresentation[H\[CapitalTheta],ZeemanBasis[]]/.PhysicalConstantValues;
{H\[Theta]evals,H\[Theta]evecs}=Eigensystem[H\[Theta]mat];
expfacs=If[htapprox,
(( 1+(-(\[HBar]*#)/(kB*T)))&/@H\[Theta]evals),
(Exp[-(\[HBar]*#)/(kB*T)]&/@H\[Theta]evals)
]/.PhysicalConstantValues;
If[((Or@@((#<htapproxwth)&/@expfacs))&&htapprox),Message[ThermalEquilibriumDensityOperator::highTapproxwarning]
];
Z=Plus@@expfacs;
X=SparseArray[Transpose[Normalize/@H\[Theta]evecs]];
Xinv=ConjugateTranspose[X];
Operator[
X . SparseArray[Band[{1,1}]->expfacs/Z] . Xinv,
ZeemanBasis[]
]];


