PolarizedDensityOperator::usage=
"PolarizedDensityOperator[<args>] sets up a density operator in which one, or several spins, are polarized along an arbitrary axis. In general the polarization levels and polarization directions may be defined individually for each spin. 
One special case of the syntax is PolarizedDensityOperator[{p,\"z\"}] which generates a density operator with polarization level p along the z-axis for all spins in the current SpinSystem[]. Similarly, PolarizedDensityOperator[{j,k,l},{p,\"z\"}] generates a density operator with polarization level p along the z-axis for spins {j,k,l}. 
The most general syntax is PolarizedDensityOperator[{{j,{polj,axisj}},{k,{polk,axisk}}..},spinsystem] which constructs a density operator for spinsystem, corresponding to polarization polj for spins j along the axis axisj, and similarly for spins k, etc. The polarizations {polj,polk..} are in the range -1 to +1, with -1 indicating complete negative polarization, and +1 indicating complete positive polarization. If a polarization argument is omitted, a value of +1 is assumed.
If spinsystem is omitted, the current SpinSystem[] is assumed. 
The spin labels {j,k..} must be members of spinsystem and cannot be duplicated.
The axis arguments may take values \"x\",\"y\", \"z\", or a number \[Phi], or a pair of polar angles {\[Theta],\[Phi]}. The phase \[Phi] denotes an axis in the transverse plane. The polar angles {\[Theta],\[Phi]} may be used to define an arbitrary direction. If an axis argument is omitted, the axis \"z\" is assumed. 
The syntax PolarizedDensityOperator[{j,k..},{pol,axis},spinsystem] assumes all spins {j,k..} have polarization level pol along axis. 
The syntax PolarizedDensityOperator[{j,k..},pol,spinsystem] assumes all spins {j,k..} have polarization level pol along the z-axis.
The syntax PolarizedDensityOperator[pol, spinsystem] assumes all spins in the spinsystem have polarization level pol along the z-axis.
The syntax PolarizedDensityOperator[axis, spinsystem] assumes all spins in the spinsystem have polarization level +1 along axis.
