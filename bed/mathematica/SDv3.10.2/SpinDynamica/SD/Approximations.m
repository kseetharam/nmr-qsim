(* ::Input::Initialization:: *)
Secularize[op_Operator,spinsets:{_List..}]:=
Module[{includedspins,missingspins},(
Secularize[op,Append[spinsets,missingspins]]
)/;(
includedspins=Union@@spinsets;
missingspins=Complement[First/@SpinSystem[],includedspins];
!MatchQ[missingspins,{}]
)];


