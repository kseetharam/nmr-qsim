(* ::Input::Initialization:: *)
Secularize[op_Operator,spinsets:{_List..}]:=
Module[{opmatrep},(
opmatrep=MatrixRepresentation[op,ZeemanBasis[]];
Operator[
SparseArray[opmatrep*SecularizationMatrix[spinsets,ZeemanBasis[]]],ZeemanBasis[]
]
