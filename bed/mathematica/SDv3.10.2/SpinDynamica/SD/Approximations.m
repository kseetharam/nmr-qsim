ThermalizeSuperoperator[sop_?SuperoperatorQ,{H\[CapitalTheta]_?OperatorQ,T:Except[_Rule]},opts___Rule]:=Module[{n,UnityH,UnityL,\[Rho]eq,\[CapitalTheta],\[CapitalGamma]mat,\[CapitalGamma]\[CapitalTheta]raw,\[CapitalGamma]\[CapitalTheta],BT},
n=BasisDimension[];
UnityH=IdentityMatrix[n,SparseArray];
UnityL=IdentityMatrix[n*n,SparseArray];
\[Rho]eq=MatrixRepresentation[ThermalEquilibriumDensityOperator[H\[CapitalTheta],T,opts],ZeemanBasis[]];
\[CapitalTheta]=
Chop[UnityL-
KroneckerProduct[Flatten[(\[Rho]eq-n^-1 UnityH)],Flatten[UnityH]]];
\[CapitalGamma]mat=N@SuperoperatorMatrixRepresentation[sop,ZeemanKetBraOperatorBasis[]];
\[CapitalGamma]\[CapitalTheta]raw=\[CapitalGamma]mat . \[CapitalTheta];
\[CapitalGamma]\[CapitalTheta]=If[NumericMatrixQ[#/.PhysicalConstantValues],#/.PhysicalConstantValues,#]&@\[CapitalGamma]\[CapitalTheta]raw;
If[OperatorBasis[]=== ZeemanKetBraOperatorBasis[],Superoperator[Simplify[\[CapitalGamma]\[CapitalTheta]],ZeemanKetBraOperatorBasis[],ZeemanBasis[],
SuperoperatorType->None],BT=OperatorBasisTransformationMatrix[OperatorBasis[],ZeemanKetBraOperatorBasis[]];Superoperator[Simplify[BT . \[CapitalGamma]\[CapitalTheta] . ConjugateTranspose[BT]],OperatorBasis[],ZeemanBasis[],
SuperoperatorType->None]]
