(* ::Input::Initialization:: *)
SecularizationMatrix[spinsets:{_List..},zb_ZeemanBasis]:=
SecularizationMatrix[spinsets,zb]=
Module[{Mvals,n},(
Mvals=MagneticQuantumNumber[#,spinsets]&/@BasisKets[zb];
n=BasisDimension[zb];
SparseArray[{{i_,j_}/;Mvals[[i]]==Mvals[[j]]->1},{n,n}]
)];


