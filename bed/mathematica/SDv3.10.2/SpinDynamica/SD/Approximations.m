(* ::Input::Initialization:: *)
Secularize::usage=
"Secularize[op,args] implements secularization of an operator, meaning the removal of terms that are non-secular with respect to a particular Hamiltonian.
The syntax Secularize[op,H0] secularizes the operator op with respect to the Hamiltonian H0. This removes all off-diagonal terms in op, when expressed in the eigenbasis of H0, unless the off-diagonal terms connect degenerate eigenvalues of H0. The syntax Secularize[op,H0,threshold] only removes off-diagonal terms if the corresponding eigenvalues differ by more than the numerical threshold.    
The syntax Secularize[op,{spins1,spins2..}] secularizes with respect to a Zeeman Hamiltonian in which the sets of spins belong to different isotopic types. If only one list of spins is present, the routine assumes a homonuclear system. 
The syntax Secularize[sop,{spins1,spins2..}] secularizes a superoperator sop with respect to a Zeeman Hamiltonian in which the sets of spins {spins1,spins2..} belong to different isotopic types. Secularization of a superoperator implies deletion of all matrix elements connecting different sets of coherence orders for the specified spins.
";


