(* ::Input::Initialization:: *)
CorrelatedEigensystems[Qmats_,opts___Rule,{"2","2"}]:=Module[{dim,len,pos,range,eigensystems,evecs,evals,ambstatus,auxfunc,correlatedevals,correlatedevecs,temp,dvalsdot},
Unprotect[Dot];
dvalsdot=DownValues[Dot];
CheckAbort[
DownValues[Dot]={};
len=Length[Qmats];
range=Range[len-1];
dim=Dimensions[First[Qmats]][[1]];
eigensystems=Chop[Eigensystem/@(Qmats)];
evals=eigensystems[[All,1]];
evecs=SparseArray[Chop[eigensystems[[All,2]]]];
ambstatus=False;
correlatedevals={First[evals]};
correlatedevecs={First[evecs]};
auxfunc=Function[{overlap,pos},
temp=Flatten[Position[#,Max[#]]&/@overlap];
If[DuplicateFreeQ[temp],
correlatedevals=Append[correlatedevals ,evals[[pos+1]][[temp]]];
correlatedevecs=Append[correlatedevecs ,evecs[[pos+1]][[temp]]];,
ambstatus=True;
temp=OptimalBipartiteGraphMatching[1-overlap/3];
correlatedevals=Append[correlatedevals ,evals[[pos+1]][[temp]]];
correlatedevecs=Append[correlatedevecs,evecs[[pos+1]][[temp]]];
]
];
Map[auxfunc[2-((#/Max[#+10^-10]&/@Outer[Abs[(Re[#1]-Re[#2])]&,correlatedevals[[#]],evals[[#+1]],1])+(#/Max[#+10^-10]&/@Outer[Abs[(Im[#1]-Im[#2])]&,correlatedevals[[#]],evals[[#+1]],1]))+(#/Max[#+10^-10]&/@Chop@Abs[Normal[correlatedevecs[[#]] . ConjugateTranspose[evecs[[#+1]]]]]),#]&,range];
DownValues[Dot]=dvalsdot;
Protect[Dot];
{SparseArray/@correlatedevals,SparseArray/@correlatedevecs},
DownValues[Dot]=dvalsdot;
Protect[Dot];]
]


